const pathFinder = (maze, i, j) => {
  if (!Array.isArray(maze)) {
    maze = maze
      .split('\n')
      .map((v) => v.trim())
      .map((v) => v.split(''));
    i = 0;
    j = 0;
  }

  if (i < 0 || i >= maze.length) return false;
  if (j < 0 || j >= maze.length) return false;
  if (maze[i][j] !== '.') return false;
  if (i === maze.length - 1 && j === maze.length - 1) return true;

  maze[i][j] = '*'; // Marked as visited

  const east = pathFinder(maze, i + 1, j);
  const west = pathFinder(maze, i - 1, j);
  const north = pathFinder(maze, i, j + 1);
  const south = pathFinder(maze, i, j - 1);

  return east || west || north || south;
};

